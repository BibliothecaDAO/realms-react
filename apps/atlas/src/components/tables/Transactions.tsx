import { Button } from '@bibliotheca-dao/ui-lib/base';
import { useStarknetTransactionManager } from '@starknet-react/core';
import Link from 'next/link';
import type { Status, TransactionStatus } from 'starknet';
import type { ENQUEUED_STATUS } from '@/constants/index';
import { getTxMessage } from '@/hooks/settling/useTxMessage';
import { ExternalLink } from '@/shared/Icons';

interface Metadata {
  description: string;
  title: string;
  action: string;
}
interface EnqueuedOrReceivedTransaction {
  metadata?: Metadata;
  status: Status | TransactionStatus | typeof ENQUEUED_STATUS;
  transactionHash?: string;
}
interface TxCartItem {
  transaction: EnqueuedOrReceivedTransaction;
  onRemove?: () => void;
}

const STYLES = {
  status: {
    REJECTED: 'bg-red-400',
    NOT_RECEIVED: 'bg-red-200',
    RECEIVED: 'bg-green-800/40 animate-pulse',
    PENDING: 'bg-orange-500 animate-pulse',
    ACCEPTED_ON_L2: 'bg-green-600/90',
    ACCEPTED_ON_L1: 'bg-green-900',
    TRANSACTION_RECEIVED: 'bg-green-400 animate-pulse',
    ENQUEUED: 'border-white/20 border-2 bg-black',
  },
} as const;

export const TxCartItem = (props: TxCartItem) => {
  const { title, description } = getTxMessage(props.transaction);

  const resolvedTitle = props.transaction.metadata?.title || title;
  const resolvedMsg = props.transaction.metadata?.description || description;

  // Multicall descriptions are generated by the TransactionQueueContext
  // as array of objects that describe how to render sub-calls.
  const descriptionIsArray = Array.isArray(resolvedMsg);

  return (
    <div
      className={`${
        STYLES.status[props.transaction.status]
      }  rounded flex p-4 w-full font-semibold mb-2`}
    >
      <div className="flex justify-between w-full p-1 rounded shadow-inner bg-black/19">
        <div>
          <h5 className="mb-3 text-xs opacity-80">
            {props.transaction.status}
          </h5>
          <h2 className="sm:text-3xl">{resolvedTitle}</h2>
          {descriptionIsArray ? (
            resolvedMsg.map((m, i) => {
              if (m.title && m.description) {
                return (
                  <div
                    className="py-2"
                    key={`${props.transaction.transactionHash}:${i}`}
                  >
                    <h4>
                      {i + 1}. {m.title}
                    </h4>
                    <p className="text-lg opacity-75">{m.description}</p>
                  </div>
                );
              }
              return <p key={i}>{m}</p>;
            })
          ) : (
            <p>{resolvedMsg}</p>
          )}
        </div>
        <div>
          {props.transaction.transactionHash ? (
            <Link
              target={'_blank'}
              rel="noreferrer noopener"
              href={
                // TODO: use network aware link using @/util/blockExplorer
                'https://goerli.voyager.online/tx/' +
                props.transaction.transactionHash
              }
            >
              <a className="p-2 tracking-widest uppercase border rounded opacity-60 border-white/30 hover:bg-white/20">
                See on Voyager <ExternalLink className="inline-block w-4" />
              </a>
            </Link>
          ) : null}
          {props.onRemove && (
            <Button
              size="sm"
              variant="outline"
              texture={false}
              onClick={props.onRemove}
            >
              Remove
            </Button>
          )}
        </div>
      </div>
      {/* <span>{props.transaction.lastUpdatedAt}</span> */}
    </div>
  );
};

export const TransactionCartTable = () => {
  const { transactions } = useStarknetTransactionManager();
  return (
    <div className="flex flex-col-reverse flex-wrap w-full">
      {transactions.map((a, index) => {
        return <TxCartItem key={index} transaction={a} />;
      })}
    </div>
  );
};
